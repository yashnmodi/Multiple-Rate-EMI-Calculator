    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        long p = Integer.parseInt(jTextField1.getText());
        long np = p;
        int t = Integer.parseInt(jTextField2.getText());
        int slab = Integer.parseInt(jTextField3.getText());
        String years[] = jTextField4.getText().split(",");
        String roi[] = jTextField5.getText().split(",");
        float yarr[] = new float[slab];
        float rarr[] = new float[slab];
        float emi[] = new float[slab];
        float inMon, pMon, interest=0;
        int mon;
        for(int i=0;i<slab;i++){
            yarr[i] = Float.parseFloat(years[i]);
            rarr[i] = Float.parseFloat(roi[i]);
        }
       
        int sum=0;
        for(int i=0;i<slab;i++){
            sum += yarr[i];
        }
        
        if(slab == yarr.length && yarr.length == rarr.length && sum == t){
            for(int i=0; i<slab; i++){
                emi[i] = calEMI(np, rarr[i], yarr[i]);
                for (mon = 1; mon <= (yarr[i] * 12); mon++) {
                    inMon  = np * (rarr[i] / 100);
                    interest += inMon;
                    pMon = emi[i] - inMon;
                    np -= pMon;
                }
            }
            jTextArea1.setText("EMI Statistics:\n");
            for(int i=0;i<slab;i++){
            jTextArea1.append("EMI for "+yarr[i]+" years @ rate of "+rarr[i]+" is Rs "+emi[i]+"\n");                
            }
        }
        else{
            jTextArea1.setText("Invalid input.");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

