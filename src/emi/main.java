    static float calEMI(float p, float monRate, float t)
    {
        float emi;
     
        monRate = monRate/(12 * 100);
        t = t * 12;
        emi = (p * monRate * (float)Math.pow(1 + monRate, t)) 
                / (float)(Math.pow(1 + monRate, t) - 1);
     
        return emi;
    }
    
